# Numpy Basics operation

import numpy as np
n_arr = np.array([[2,4,5],[10,4,6],[1,8,3]])

print(n_arr.ndim) # Gives the dimesnsion of array 
print(n_arr.size)  # Size of the array
print(n_arr.shape) # Gives the shape of the array
print(n_arr.dtype.name) # Gives the data type of the element of the array

# Array Substraction

